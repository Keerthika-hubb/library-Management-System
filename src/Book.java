public class Book {

    private int bookId;
    private String title;
    private String author;
    private String category;
    private String status;
    public Book(int bookId, String title, String author, String category, String status) {
        this.bookId = bookId;
        this.title = title;
        this.author = author;
        this.category = category;
        this.status = status;

    }

}
